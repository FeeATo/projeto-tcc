    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        
        con = new ClasseConexaoJava();
        boolean resultado = con.conectar();
        if(resultado == true){
            try{
                String query = "select senha, nome from ADM where login_adm=?";
                PreparedStatement pst = con.getConn().prepareStatement(query);
                pst.setString(1, txtLogin.getText());
                
                ResultSet rs = pst.executeQuery();
                while(rs.next()){
                    output = rs.getString("senha");
                    nome_adm = rs.getString("nome");
                }
            }
            catch (Exception erro){
                JOptionPane.showMessageDialog(null, erro);
            }
            
            if(txtSenha.getText().equals(output.replace(" ", ""))){    
                SetLoginName = txtLogin.getText();
                new jfPaginaInicial().setVisible(true);
                this.setVisible(false);
                dispose();
                
           
            } else {
                JOptionPane.showMessageDialog(null, "Login ou senha incorreto!");
            }
            
            
        }
        
        
        
        
    }//GEN-LAST:event_btnEntrarActionPerformed

    
    
